namespace LegoBluetooth
{
    /// <summary>
    /// Represents the Hub Property Reference.
    /// </summary>
    public enum HubProperty
    {
        /// <summary>
        /// Advertising Name.
        /// </summary>
        AdvertisingName = 0x01,

        /// <summary>
        /// Button.
        /// </summary>
        Button = 0x02,

        /// <summary>
        /// Firmware Version.
        /// </summary>
        FWVersion = 0x03,

        /// <summary>
        /// Hardware Version.
        /// </summary>
        HWVersion = 0x04,

        /// <summary>
        /// RSSI.
        /// </summary>
        RSSI = 0x05,

        /// <summary>
        /// Battery Voltage.
        /// </summary>
        BatteryVoltage = 0x06,

        /// <summary>
        /// Battery Type.
        /// </summary>
        BatteryType = 0x07,

        /// <summary>
        /// Manufacturer Name.
        /// </summary>
        ManufacturerName = 0x08,

        /// <summary>
        /// Radio Firmware Version.
        /// </summary>
        RadioFirmwareVersion = 0x09,

        /// <summary>
        /// LEGO Wireless Protocol Version.
        /// </summary>
        LEGOWirelessProtocolVersion = 0x0A,

        /// <summary>
        /// System Type ID.
        /// </summary>
        SystemTypeID = 0x0B,

        /// <summary>
        /// Hardware Network ID.
        /// </summary>
        HWNetworkID = 0x0C,

        /// <summary>
        /// Primary MAC Address.
        /// </summary>
        PrimaryMACAddress = 0x0D,

        /// <summary>
        /// Secondary MAC Address.
        /// </summary>
        SecondaryMACAddress = 0x0E,

        /// <summary>
        /// Hardware Network Family.
        /// </summary>
        HardwareNetworkFamily = 0x0F
    }
}



