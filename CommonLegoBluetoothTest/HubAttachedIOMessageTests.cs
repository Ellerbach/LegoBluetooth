using System;
using LegoBluetooth;
#if NANOFRAMEWORK_1_0
using nanoFramework.TestFramework;
#endif

namespace CommonLegoBluetoothTest
{
    [TestClass]
    public class HubAttachedIOMessageTests
    {
        [TestMethod]
        [DataRow(new byte[] { 0x05, 0x01, 0x04, 0x02, 0x00 }, typeof(ArgumentException))]
        [DataRow(new byte[] { 0x05, 0x01, 0x04, 0x02 }, typeof(ArgumentException))]
        //[DataRow(new byte[] { 0x05, 0x01, 0x04, 0x02, 0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00 }, null)]
        public void Decode_ShouldHandleVariousData(byte[] data, Type expectedExceptionType)
        {
            // Act & Assert
            if (expectedExceptionType != null)
            {
                try
                {
                    HubAttachedIOMessage.Decode(data);
                }
                catch (Exception ex)
                {
                    Assert.IsInstanceOfType(ex, expectedExceptionType);
                }
            }
            else
            {
                var message = HubAttachedIOMessage.Decode(data);
                Assert.IsNotNull(message);
            }
        }

        [TestMethod]
        [DataRow(new byte[] { 0x0F, 0x00, 0x04, 0x00, 0x01, 0x27, 0x00, 0x00, 0x00, 0x00, 0x10, 0x00, 0x00, 0x00, 0x10 })]
        [DataRow(new byte[] { 0x0F, 0x00, 0x04, 0x01, 0x01, 0x27, 0x00, 0x00, 0x00, 0x00, 0x10, 0x00, 0x00, 0x00, 0x10 })]
        [DataRow(new byte[] { 0x0F, 0x00, 0x04, 0x02, 0x01, 0x25, 0x00, 0x00, 0x00, 0x00, 0x10, 0x00, 0x00, 0x00, 0x10 })]
        [DataRow(new byte[] { 0x0F, 0x00, 0x04, 0x03, 0x01, 0x26, 0x00, 0x00, 0x00, 0x00, 0x10, 0x00, 0x00, 0x00, 0x10 })]
        [DataRow(new byte[] { 0x09, 0x00, 0x04, 0x10, 0x02, 0x27, 0x00, 0x00, 0x01 })]
        [DataRow(new byte[] { 0x0F, 0x00, 0x04, 0x32, 0x01, 0x17, 0x00, 0x00, 0x00, 0x00, 0x01, 0x06, 0x00, 0x00, 0x20 })]
        [DataRow(new byte[] { 0x0F, 0x00, 0x04, 0x3A, 0x01, 0x28, 0x00, 0x00, 0x00, 0x00, 0x10, 0x00, 0x00, 0x01, 0x02 })]
        [DataRow(new byte[] { 0x0F, 0x00, 0x04, 0x3B, 0x01, 0x15, 0x00, 0x02, 0x00, 0x00, 0x00, 0x00, 0x00, 0x01, 0x00 })]
        [DataRow(new byte[] { 0x0F, 0x00, 0x04, 0x3C, 0x01, 0x14, 0x00, 0x02, 0x00, 0x00, 0x00, 0x00, 0x00, 0x01, 0x00 })]
        [DataRow(new byte[] { 0x0F, 0x00, 0x04, 0x46, 0x01, 0x42, 0x00, 0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x10 })]
        public void Decode_ShouldHandleSpecificData(byte[] data)
        {
            // Act
            var message = HubAttachedIOMessage.Decode(data);

            // Assert
            Assert.IsNotNull(message);

            // Act again to test the ToByteArray method
            var result = message.ToByteArray();

            // Assert
            CollectionAssert.AreEqual(data, result);
        }
    }
}
