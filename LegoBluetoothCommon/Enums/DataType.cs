namespace LegoBluetooth
{
    /// <summary>
    /// Represents the different types of data types.
    /// </summary>
    public enum DataType
    {
        /// <summary>
        /// 8-bit value.
        /// </summary>
        Byte = 0,

        /// <summary>
        /// 16-bit unsigned integer value.
        /// </summary>
        UInt16 = 1,

        /// <summary>
        /// 32-bit unsigned integer value.
        /// </summary>
        UInt32 = 2,

        /// <summary>
        /// 32-bit floating point value.
        /// </summary>
        Single = 3,
    }
}
